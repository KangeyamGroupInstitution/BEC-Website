  var phyMarks = document.getElementById("phy");
  var cheMarks = document.getElementById("che");
  var matMarks = document.getElementById("mat");
  var h6 = document.createElement('h6');
function calculateCutoff() {
   if(phyMarks.value>100){
    
    const errors = document.getElementsByClassName('error'); 
    errors.item(0).appendChild(h6).innerHTML = `Please Enter Valid Mark`;
    phyMarks.value = undefined;
    document.getElementById("cut_off").value = undefined; 
  }
  else if(cheMarks.value>100){
    const errors = document.getElementsByClassName('error'); 
    errors.item(1).appendChild(h6).innerHTML = `Please Enter Valid Mark`;
    cheMarks.value = undefined;
  }
  else if(matMarks.value>100){
    const errors = document.getElementsByClassName('error'); 
    errors.item(2).appendChild(h6).innerHTML = `Please Enter Valid Mark`;
    matMarks.value = undefined;
  }
  else{
    const mathPercent = (matMarks.value / 100) * 100;
    const chemPercent = (cheMarks.value / 100) * 50; 
    const physPercent = (phyMarks.value / 100) * 50;
    const cutoff = mathPercent + chemPercent + physPercent;
    const cutoffOutOf200 = Math.min(cutoff, 200); 
   
    document.getElementById("cut_off").value = cutoffOutOf200; 
    h6.remove()
  }
  
}

document.getElementById("phy").addEventListener("input", calculateCutoff);
document.getElementById("che").addEventListener("input", calculateCutoff);
document.getElementById("mat").addEventListener("input", calculateCutoff);
